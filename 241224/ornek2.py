kisakenar=int(input("kısa kenarı giriniz")) #kullanıcıdan kısakenarı alınız
uzunkenar=int(input("uzunkenarı giriniz"))  #kullanıcıdan uzunkenarı alınız 
alan=kisakenar*uzunkenar  #alan hesaplandı
cevre=(kisakenar+uzunkenar)*2  #çevre hesaplandı
print("dikdörtgenin alanı:",alan) #alan ekrana yazdırıldı
print("dikdörtgenin çevresi:",cevre)  #çevre akrana yazdırıldı
